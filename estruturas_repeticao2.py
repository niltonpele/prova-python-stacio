# Criação da variável texto
texto = 'Olá, laço for.'

# Laço for para iterar sobre cada caractere da variável texto
for item in texto:
    print('Caractere: ' + item)

# Laço for para iterar sobre um intervalo numérico de 1 a 10
for numero in range(1, 11):
    print('Número do intervalo: ' + str(numero))