# Cria uma variável chamada tempoExperiencia e atribui a ela o valor 5
tempoExperiencia = 3

# Verifica se o tempo de experiência é menor que 2
if tempoExperiencia < 2:
    # Se a condição for verdadeira, imprime a mensagem
    print('Nível de conhecimento júnior.')
# Verifica se o tempo de experiência é maior que 2 e menor que 5
elif 2 < tempoExperiencia < 5:
    # Se a condição for verdadeira, imprime a mensagem
    print('Nível de conhecimento pleno.')
# Se nenhuma das condições anteriores for verdadeira
else:
    # Imprime a mensagem
    print('Nível de conhecimento sênior.')